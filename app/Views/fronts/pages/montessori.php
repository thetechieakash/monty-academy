                    Named for its founder, Dr. Maria Montessori, the Montessori method is based on two simple truths: That <b>children must be respected,</b> and that <b>children spontaneously love learning.</b>
                </p>
                <p class="dc-para">
                    These principles and careful observation form a child-centered method that Montessori called an <b>“education for life.”</b> Its goal is the finest development of the whole human being – emotionally, physically, intellectually and spiritually – toward the nurturing of peaceful, caring citizens.
                </p>
                <p class="dc-para">
                    The Montessori prepared environment honors the child and the beauty and order essential for him to work at his natural, individual and optimal level. Carefully designed Montessori materials attract the interest of the student, while at the same time teaching an important, isolated concept for the child’s discovery. The child constructs her own reality and awareness, at first concretely through hands-on manipulation, until patterns are internalized and she discovers the next level of abstraction. The integrated Montessori curriculum shows the child how every aspect of learning is connected and intertwined. <b>The Montessori educator understands and guides the child</b> without interfering in her natural ability to teach herself and become an independent, contributing member in the “cosmic plan.”
