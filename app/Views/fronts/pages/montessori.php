                <h3>More on Doctor Maria Montessori</h3>
                <p>Maria Montessori is often referred to as "ahead of her time". Born in Italy in 1870, Maria Montessori moved to Rome with her parents in 1875 at the age of five. Although her father Alessandro embraced traditional views of female education, it was the more liberal approach of her mother, Renilde Montessori, that <b>encouraged Maria Montessori to explore her natural inclination to learn,</b> regardless of the social restrictions placed on women in the male-dominated society of the day. In so doing, Renilde played an active role in her daughter's upbringing, and indeed, <b>the whole philosophy behind what is now known as the "Montessori Method."</b></p>
            </div>
        </div>
        <div class="row align-items-center">
            <div class="col-md-8">
                <p>Studies in math, physics, natural sciences, biology and medicine led Maria Montessori to apply to the College of Medicine at the University of Rome, and she became the first female certified physician in Italy in 1896, graduating at the top of her class. In addition to her duties as a doctor, Maria Montessori conducted research work in psychiatric medicine and continued her education in philosophy, psychology and education. She was appointed professor of anthropology at the University of Rome in 1904.</p>
                <p>Throughout, her interest in the development of children grew - first from her experience with disabled children and the deplorable state of their care at the time, then further with mentally-challenged children in her care. As she learned from the work of others already accomplished in the area of early childhood education, <b>her own theories evolved, embracing elements, ideas and methods of all disciplines she had studied.</b></p>
                <p>In 1906, at the age of 36, Maria Montessori founded the first Casa dei Bambini, or "Children's House" for children of the industrial revolution's working-class in one of the city's worst slum districts. With some 60 children in her care, Maria Montessori began their education by <b>instructing the older children on how to help out with everyday chores.</b> Sense materials that she had developed previously were introduced, and to her surprise, Montessori discovered how naturally young children adapted and enjoyed learning everyday tasks. The structure of work and constructive activity gave the children a sense of self-worth that they had never before experienced.</p>
