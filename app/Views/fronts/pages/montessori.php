                <p>One of Dr. Maria Montessori's first major hurdles to improve the lives of these children was accomplished by encouraging parents to recognize that their children were special and of great value. <b>From this reverence for the individual beauty and potential grew the Montessori Method.</b> Critical periods of early childhood development were identified through her observations, and the methodology evolved to address these periods with age-appropriate learning tools and activities. Further development of the methodology embraced what Montessori described as the "cosmic education" - where children would be given the environment and guidance to become the peacemakers of the future, existing in harmony with all living beings in a sustainable world.</p>
                <p>From this time to her death in 1952, Maria Montessori continued her work, which became widely recognized and embraced throughout the United States, Europe, and India. She conducted and founded training courses on these continents, established a research institute in Spain, and developed Montessori Training Centers in the Netherlands and London.</p>
